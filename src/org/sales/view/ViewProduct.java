    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
       int id=Integer.parseInt(jTextField_EId.getText());
       String name=jTextField_EName.getText();
        double price =Double.parseDouble(jTextField_EPrice.getText());
        int qty =Integer.parseInt(jTextField_EQty.getText());
        String remarks=jTextArea_ERemarks.getText();
        //Set These Values In the "Model Class- product " Object
        Product ob=new Product();
        ob.setId(id);
        ob.setName(name);
        ob.setPrice(price);
        ob.setQty(qty);
        ob.setRemarks(remarks);
        
        //Now handover This Object To the "Contoller Class  ProductDAO"
        ProductDAO pDAO=new ProductDAO();
       int count = pDAO.updateData(ob);
        if(count>0)
        {
            JOptionPane.showMessageDialog(null,"Record Updated");
            loadData();
        }
        clearFields();
    
    }//GEN-LAST:event_jButton_UpdateActionPerformed

