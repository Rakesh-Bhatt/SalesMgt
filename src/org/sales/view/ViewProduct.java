    public void loadData()
    {
        ProductDAO pDAO=new ProductDAO();
            List<Product>productList=pDAO.fetchData();
            clearTable();
            for(Product p:productList)
            {
                
                model.addRow(new Object[]{
                    p.getId(),p.getName(),p.getPrice(),p.getQty(),p.getRemarks()});
            }
    }
    public void clearFields()
    {
        //jTextField_EID.setText("");
        jTextField_EName.setText("");
        jTextField_EPrice.setText("");
        jTextField_EQty.setText("");
        jTextArea_ERemarks.setText("");
    }
    public void clearTable()
    {
        int count=jTable_Product.getRowCount();
        for(int i=0;i<count;i++)
        {
            model.removeRow(0);
        }
    }

